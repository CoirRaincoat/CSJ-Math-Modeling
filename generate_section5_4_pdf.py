pdf.body('对贪心搜索得到的最优候选方案，进行100次迭代的爬山法局部优化。每次迭代随机选择以下三种操作之一：替换操作——随机选取套餐中的一个菜品，替换为同类别中另一个候选菜品；添加操作——在预算允许范围内（总价 ≤ 目标价位 × 0.9），从菜品库中选取一个新菜品加入套餐；移除操作——从套餐中随机移除一个菜品（仅在菜品数 > 最少菜品数时执行）。每次操作后重新计算S(C)，仅当新方案的评分高于当前最佳方案时才接受（爬山法的核心原则：只接受更优解）。')

